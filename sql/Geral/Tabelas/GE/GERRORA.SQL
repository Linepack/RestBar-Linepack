CREATE TABLE LINEPACK.GEERRORA
(
	CD_ERRO  VARCHAR2(30)  NOT NULL ,
	DS_ERRO  VARCHAR2(2000)  NOT NULL ,
	CA_ERRO  VARCHAR2(2000)  NULL ,
	AC_ERRO  VARCHAR2(2000)  NULL ,
	NM_USUINC  VARCHAR2(30)  NOT NULL ,
	DT_USUINC  DATE  NOT NULL ,
CONSTRAINT  PK_GEERRORA PRIMARY KEY (CD_ERRO)
);

CREATE PUBLIC SYNONYM GEERRORA FOR GEERRORA;

GRANT ALL ON GEERRORA TO LINEPACK_ROLE;
